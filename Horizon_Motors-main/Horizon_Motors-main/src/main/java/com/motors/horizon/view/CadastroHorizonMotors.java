    public boolean oneChar(String pass) {
        for (int i = 0; i < pass.length(); i++) {
            if (Character.isLowerCase(pass.charAt(i))) {
                return true;
            }
        }

        return false;
    }

    public boolean oneNumber(String pass) {
        for (int i = 0; i < pass.length(); i++) {
            if (Character.isDigit(pass.charAt(i))) {
                return true;
            }
        }

        return false;
    }

    public boolean noChar(String input) {
        for (int i = 0; i < input.length(); i++) {
            if (!Character.isDigit(input.charAt(i))) {
                return true;
            }
        }
        return false;
    }

    public boolean checkEmail(String input) {
        for (int i = 0; i < input.length(); i++) {
            if (input.charAt(i) == '@') {
                return false;
            }
        }
        return true;
    }

