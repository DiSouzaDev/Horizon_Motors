    private void botaoCadastroTelaCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastroTelaCadastroActionPerformed
        String nome = jTextNome.getText();
        String cpf = jTextCpf.getText();
        String email = jTextEmail.getText();
        String tel = jTextTel.getText();

        if ("".equals(nome) || "".equals(cpf) || "".equals(email) || "".equals(tel)) {
            jLabelAviso.setText("Preencha todos os campos");
            jLabelAviso.setForeground(Color.red);
        } else if (cpf.length() > 11 && cpf.length() < 11 || noChar(cpf)) {
            jLabelCpf.setText("11 números*");
            jLabelCpf.setForeground(Color.red);
        } else if (checkEmail(email)) {
            jLabelEmail.setText("Email inválido*");
            jLabelEmail.setForeground(Color.red);
        } else if (tel.length() > 11 && tel.length() < 11 || noChar(tel)) {
            jLabelTel.setText("11 números*");
            jLabelTel.setForeground(Color.red);
        } else {
            String pass = jPassword.getText();
            if (!"".equals(pass) && pass.length() >= 8 && oneChar(pass) && oneNumber(pass)) {
                String confirmPass = jConfirmPassword.getText();

                if (pass.equals(confirmPass)) {
                    UsuarioDao user = new UsuarioDao();

                    if (user.checkLogin(cpf)) {
                        JOptionPane.showMessageDialog(null, "Usuário já cadastrado!");
                    } else {
                        try {
                            user.createUsuario(jTextNome.getText(), jPassword.getText(), jTextEmail.getText(), jTextCpf.getText(), jTextTel.getText());
                        } catch (Exception e) {
                            throw new RuntimeException("Erro na conexão: ", e);
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "As duas senhas precisam ser iguais!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "A senha precisa ter pelo menos:\n- 8 digitos\nUma letra maiúscula\nUm número");
                jPassword.setText("");
            }

        }
    }//GEN-LAST:event_botaoCadastroTelaCadastroActionPerformed

